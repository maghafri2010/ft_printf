#include "printf.h"

int    ft_putnbr(int nb)
{
    int x;
    
    x = 0;
    if (nb == -2147483648)
    {
        write(1, "-2147483648", 11);
        return 11;
    }

    if (nb < 0)
    {
        write(1, "-", 1);
        nb = -nb;
    }
    if (nb >= 10)
    {
        ft_putnbr(nb / 10);

    }

    x = ft_putchar((nb % 10) + '0');

    return x;

    
}